def calculate_fuel(mass: int):
    return mass // 3 - 2


def calc_fuel_recursively(mass: int):
    fuel = calculate_fuel(mass)
    total = fuel
    while True:
        fuel = calculate_fuel(fuel)
        if fuel > 0:
            total += fuel
        else:
            break
    return total


